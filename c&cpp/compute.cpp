void bin2hex(unsigned char* in, char* out) {
	const char hex[] = "0123456789ABCDEF";
	for (int i = 0; i < 32; i++) {
		out[2 * i] = hex[(in[i] >> 4) & 0x0F];  
		out[2 * i + 1] = hex[in[i] & 0x0F];     
	}
	out[32] = '\0'; 
}
